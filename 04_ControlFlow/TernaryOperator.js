
// condition ? True Statement : False Statement

let anyVal = 0
anyVal = 45
anyVal ? console.log(`anyVal = ${anyVal}`) : console.log(`anyVal = ZERO`)

/*
NOTE : ? is Ternary operator. It's syntax is: condition ? True Statement : False Statement
       ?? is Nullish Coalescing Operator. It's syntax is: undefined/null ?? any valid value
*/